#include "libft.h"

int	ft_error_msg(const char *msg)
{
    if (msg)
    {
        while (*msg)
        {
			write(2, "\033[31m", 5);
            write(2, msg, 1);
			write(2, "\033[0m", 4);
            msg++;
        }
    }
	return (-1);
}
